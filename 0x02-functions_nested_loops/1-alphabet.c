#include "main.h"

/**
 * print_alphabet - Prints the alphabet in lowercase.
 */
void print_alphabet(void)
{
	char i = 'a';

	for (i = 'a'; i <= 'z'; i++)
	{
		putchar(i);
	}
	putchar('\n');
	return (0);
}

/**
 * main - entry point
 * Description: welo
 * Return: (0)
 */
int main(void)
{
	void print_alphabet(void);
	return (0);
}
